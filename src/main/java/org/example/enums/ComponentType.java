package org.example.enums;

/**
 * Any component can have a type. A type is used to enforce constraints.
 * Example.
 * A RAM component can have
 *  type1 = uses technology DDR2
 *  type2 = uses technology DDR3
 * A CPU can have
 *  type3 = 32 bit architecture
 *  type4 = 64 bit architecture
 *
 */
public enum ComponentType {
    TYPE1,
    TYPE2,
    TYPE3,
    TYPE4
}
